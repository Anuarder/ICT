        <h3><a id="Modems_193"></a>Modems</h3>
        <p>Modems stands for ‘Modulator Demodulator’. Modems used to connect computers to the internet before we had the use of broadband connections.</p>
        <p>Most internet connections are made over telephone lines. Telephone lines are designed to carry sound and voices, which are analogue signals.</p>
        <p>The problem is that computer data is digital and it is not possible to send digital data over an analogue telephone line.</p>
        <p>This is where the modem comes in.<br>
        The modern convert can convert the digital computer data into an analogue signal/noise (modulate) so it can be sent over the analogue telephone line.<br>
        Modems can also reverse this process and convert the analogue signal from the telephone line unto digital data (demodulate) so the computer ca use it.</p>
        <p>Modems contain both a Digital to Analogue Converter (DAC) and a Analogue to Digital converter (ADC). DAC converts the digital data from the computer into an analogue signal which can then be sent down the telephone line. ADC converts the analogue signal in the telephone line into digital data so that the computer can process and use the data.</p>
