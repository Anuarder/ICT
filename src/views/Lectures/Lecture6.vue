        <h3><a id="Router_145"></a>Router</h3>
        <p>Routers enable data to be sent between different types of networks. Routers are most commonly used to connect computers and devices to the internet. Computers can connect to a router either through cable or wirelessly.</p>
        <p class="img-block"><img src="../../assets/router.jpg" alt=""></p>
        <p>All computers and devices connected to the internet are assigned a unique number called at Internet Protocol Address (IP Address). The IP address of the device would depend on where in the world it connected to the internet from. Computers on the same network would share the same first few numbers of the IP address. This is used to locate the network.</p>
        <p>Routers store IP addresses in something called a routing table. The routing table lists all of the different routes to other networks. The router will use the routing table to determine the best route to use when sending data to another network.</p>
        <p>Routers send ‘packets’ of data between computers on different networks. Each data packet contains the IP address of the computer / network that the data is being sent to. The router will use the IP address to work out the best route in which to send the data to it’s destination.</p>
