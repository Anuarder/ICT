        <h4><a id="Wifi_267"></a>Wi-fi</h4>
        <p>Wi-Fi allows computer/devices to connect to each other wirelessly, without using cable or wires. The computers communicate with each other by using signals that are broadcast using radio waves.<br>
        In order to use Wi-Fi, you need to have access to the internet and a router. Areas where computer/devices can access the internet using Wi-Fi are known as hotspots.<br>
        Routers are commonly used to provide a hotspot for other computers to connect to the internet. The router will be connected to the internet through an ISP and will transmit a wireless signal that wireless devices nearby can tap into. This allows multiple devices to share the same internet connection.<br>
        Routers can only broadcast the wireless signal over a limited distance. If a device is out of range, it will not able to tap into the signal. The range limit of a typical route is between 50m and 100m.</p>
        <p>In order to connect to a network using Wi-Fi you would need:</p>
        <ul>
            <li>A computer or device;</li>
            <li>A wireless network interface card;</li>
            <li>Access to a wireless hotspot.</li>
        </ul>
